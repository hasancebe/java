package DAY04calisma;

public class C01_increment {
    public static void main(String[] args) {
        int a=10;
        System.out.println(++a);
        a+=3;
        System.out.println(a);
        a%=4;
        System.out.println(a);

        int sayi=10;
        int sayi1=sayi++;
        System.out.println(sayi);
        System.out.println(sayi1);

    }
}
